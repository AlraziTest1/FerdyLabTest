public class Belajarincrement{
	public static void main(String[] args){
		int a = 25;
		int b = 30;
		
		System.out.println("# Post Increment #");		
		System.out.println("=================");		
		System.out.println("Isi variabel a: " + a);		
		System.out.println("Isi variabel a: " + a++);	
		System.out.println("Isi variabel a: " + a);		
	
		System.out.println();
		
		System.out.println("# Pre Increment #");		
		System.out.println("=================");		
		System.out.println("Isi variabel b: " + b);		
		System.out.println("Isi variabel b: " + ++b);	
		System.out.println("Isi variabel b: " + b);
		
		
	
	
	
	}
}