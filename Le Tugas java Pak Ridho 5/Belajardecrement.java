public class Belajardecrement{
	public static void main(String[] args){
		int c = 35;
		int d = 40;
		
		System.out.println("# Pre Decrement #");		
		System.out.println("=================");		
		System.out.println("Isi variabel c: " + c);		
		System.out.println("Isi variabel c: " + --c);	
		System.out.println("Isi variabel c: " + c);
	
		System.out.println();		
	
		System.out.println("# Post Increment #");		
		System.out.println("=================");		
		System.out.println("Isi variabel d: " + d);		
		System.out.println("Isi variabel d: " + d--);	
		System.out.println("Isi variabel d: " + d);		
	
	
	
	}
}