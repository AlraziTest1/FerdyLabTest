public class BelajarJava2 {
	public static void main(String args[]){
		int a = 5;	//	nilai int a adalah 5
		int b = 10;	//	nilai int a adalah 10
		
		if (a > b) {	//	memanggil funsgsi if "jika nilai variabel a lebih besar dari variabel b,maka jalankan {...}"
			System.out.println("Nilai variable a lebih besar dari variabel b");	/*	yang akan dijalankan bila kondisi fungsi "if" terpenuhi*/
		}
		if (a < b) {	//	memanggil funsgsi if "jika nilai variabel a kecil besar dari variabel b,maka jalankan {...}"
			System.out.println("Nilai variable a lebih kecil dari variabel b");	/*	yang akan dijalankan bila kondisi fungsi "if" terpenuhi*/
		}
		if (a == b) {	//	memanggil funsgsi if "jika nilai variabel a bernilai sama dengan variabel b,maka jalankan {...}"
			System.out.println("Nilai variable a sama dengan variabel b");		/*	yang akan dijalankan bila kondisi fungsi "if" terpenuhi*/
		}
		
	}
}
		
