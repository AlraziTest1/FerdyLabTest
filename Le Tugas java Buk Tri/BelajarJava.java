public class BelajarJava{
	public static void  main(String[] args){
		int a=10;
		int b=5;
		int hasil;
		
		hasil= a + b;
		System.out.println(hasil);
		
		hasil= a - b;
		System.out.println(hasil);
		
		hasil= a * b;
		System.out.println(hasil);
		
		hasil= a / b;
		System.out.println(hasil);
		
		hasil= a & b;
		System.out.println(hasil);
	}
}