public class OperatorAritmatika5{
	public static void main(String[] args){
		int angka1 = 5;
		int angka2 = 7;
		
		int nilai1 = 10;
		int nilai2 = 5;
		
		int kali1 = 4;
		int kali2 = 3;
		
		int x = 10;
		int y = 2;

		int hasil;
		
		hasil = angka1 + angka2;
		System.out.println(hasil);
		
		hasil = nilai1 - nilai2;
		System.out.println(hasil);
		
		hasil = kali1 * kali2;
		System.out.println(hasil);
		
		hasil = x / y;
		System.out.println(hasil);
		
	}
}