public class Belajarperbandingan{
	public static void main(String args[]){
		int a = 25;
		int b = 25;
		boolean jancokPristel;
		
		jancokPristel = a == b;
		System.out.println("Apakah a == b ? " + jancokPristel);
		
		
		jancokPristel = a != b;
		System.out.println("Apakah a != b ? " + jancokPristel);
		
		
		jancokPristel = a > b;
		System.out.println("Apakah a > b ? " + jancokPristel);
		
		
		jancokPristel = a < b;
		System.out.println("Apakah a < b ? " + jancokPristel);
		
		
		jancokPristel  = a >= b;
		System.out.println("Apakah a >= b ? " + jancokPristel);
		
		
		jancokPristel = a <= b;
		System.out.println("Apakah a <= b ? " + jancokPristel);
		
	}
	
}